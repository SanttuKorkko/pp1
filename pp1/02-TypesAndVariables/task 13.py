# Calculation of the area and circumference of a circle

# determine radius and PI
radius = 5
PI = 3.141592653589793

# calculate area 
area = PI*radius**2

# calculate circumference 
circumference = 2*PI*radius

# display results 
txt = "If circle's radius is {}, it's area is {} and circumference is {}".format(radius, area, circumference)
print(txt)